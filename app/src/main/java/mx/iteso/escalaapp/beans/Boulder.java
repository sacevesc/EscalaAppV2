package mx.iteso.escalaapp.beans;

public class Boulder {
    private String boulder_key;
    private String bonus, top;
    private String time;
    private String number;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getBoulder_key() {
        return boulder_key;
    }

    public void setBoulder_key(String boulder_key) {
        this.boulder_key = boulder_key;
    }


    public String getBonus() {
        return bonus;
    }

    public void setBonus(String bonus) {
        this.bonus = bonus;
    }

    public String getTop() {
        return top;
    }

    public void setTop(String top) {
        this.top = top;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }


}
